class Solution
{
public:
    vector<int> singleNumber(vector<int> nums) 
    {
        vector<int> res;
        map<int,int> hashmap; //key=elements and value=frequency
        for(auto it:nums){
            hashmap[it]++;              
        }
        for(auto pair:hashmap){
            if(pair.second==1){           // frequency is one
                res.push_back(pair.first);    // add that key to res vector
            }
        }
        return res;
    }
};

