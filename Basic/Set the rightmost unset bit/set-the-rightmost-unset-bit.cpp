class Solution
{
public:
    int setBit(int N)
    {
        for(int i = 0; i < 31; i++){
            if((N & 1 << i) == 0){
                return N | 1 << i;
            }
        }
    }
};

//{ Driver Code Starts.
int main()
{
    int t;
    cin >> t;
    while (t--)
    {
        int N;
        cin >> N;
        Solution ob;
        int ans = ob.setBit(N);
        cout << ans << endl;
    }
    return 0;
}
