class Solution
{
    public int longestPalinSubseq(String S)
    {
        String R = new StringBuilder(S).reverse().toString();
        int m = S.length();
        int n = R.length();
        int dp[][] = new int[m + 1][n + 1];
        for(int i = 1; i <= m; i++){
            for(int j = 1; j <= n; j++){
                if(S.charAt(i - 1) == R.charAt(j - 1)){
                    dp[i][j] = dp[i - 1][j - 1] + 1;
                }
                else{
                    dp[i][j] = Math.max(dp[i - 1][j], dp[i][j - 1]);
                }
            }
        }
        return dp[m][n];
    }
}